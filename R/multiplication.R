#' Multiplication
#'
#' @param value_1 first number input
#' @param value_2 second number input
#'
#' @return product of two input numbers
#' @export
#'
#' @examples multiplication(value_1 = 3, value_2 = 4)
multiplication <- function(value_1, value_2) {
  product = value_1 * value_2
  print(product)
}
